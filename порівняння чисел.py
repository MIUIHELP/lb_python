# Введення  числа 1
num1 = float(input("Введіть число №1: "))

# Введення  числа 2
num2 = float(input("Введіть число №2: "))

# Порівняння чисел
if num1 > num2:
    print("Число №1 більше за число №2.")
elif num1 < num2:
    print("Число №2 більше за число №1.")
else:
    print("Числа рівні.")